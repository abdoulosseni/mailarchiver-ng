// i18n minimaliste (sans dépendance) — gestion FR / EN.
//
// `locale` est un ref réactif : tout appel à `t()` dans un template se
// re-évalue automatiquement au changement de langue. La préférence est
// persistée dans localStorage (clé `ma_lang`) ; à défaut, on tente la langue
// du navigateur, puis on retombe sur le français.

import { ref } from 'vue'

const STORAGE_KEY = 'ma_lang'
export const SUPPORTED = ['fr', 'en']
const DEFAULT = 'fr'

function detectInitial() {
  const saved = localStorage.getItem(STORAGE_KEY)
  if (saved && SUPPORTED.includes(saved)) return saved
  const nav = (navigator.language || '').slice(0, 2).toLowerCase()
  return SUPPORTED.includes(nav) ? nav : DEFAULT
}

export const locale = ref(detectInitial())

export function setLocale(l) {
  if (!SUPPORTED.includes(l)) return
  locale.value = l
  localStorage.setItem(STORAGE_KEY, l)
  document.documentElement.setAttribute('lang', l)
}

// Applique l'attribut <html lang> au chargement (cohérence accessibilité/SEO).
export function initI18n() {
  document.documentElement.setAttribute('lang', locale.value)
}

// Traduction. `key` est une clé pointée (ex. "settings.dlq.title").
// `params` interpole les placeholders `{nom}` présents dans la chaîne.
export function t(key, params) {
  const dict = messages[locale.value] || messages[DEFAULT]
  let str = resolve(dict, key)
  if (str == null) str = resolve(messages[DEFAULT], key) // repli FR
  if (str == null) return key
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      str = str.replaceAll('{' + k + '}', v)
    }
  }
  return str
}

function resolve(dict, key) {
  return key.split('.').reduce((o, k) => (o == null ? o : o[k]), dict)
}

export function useI18n() {
  return { t, locale, setLocale }
}

